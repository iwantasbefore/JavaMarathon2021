package day5;

public class Car {
    private int year;
    private String color;
    private String model;

    public void setYear(int useryear) {
        year = useryear;
    }

    public int getYear() {
        return year;
    }

    public void setColor(String usercolor) {
        color = usercolor;
    }

    public String getColor() {
        return color;
    }

    public void setModel(String usermodel) {
        model = usermodel;
    }

    public String getModel() {
        return model;
    }

}

